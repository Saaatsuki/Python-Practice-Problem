'''

\n: 改行（新しい行へ移動）
\t: 水平タブ（文字をタブ幅分進める）
\\: バックスラッシュ（\文字そのものを表現）
\': シングルクォーテーション（シングルクォートを文字列内で使用）
\": ダブルクォーテーション（ダブルクォーテーションを文字列内で使用）
\r: キャリッジリターン（カーソルを行の先頭に移動）
\b: バックスペース（1文字分戻る）

'''


# 改行とタブの使用例
print("Hello\nWorld")  # HelloとWorldを改行して表示
print("Hello\tWorld")  # HelloとWorldの間にタブを挿入して表示

# バックスラッシュの使用例
print("This is a backslash: \\")  # バックスラッシュを表示する

# シングルクォーテーションとダブルクォーテーションの使用例
print('He said, "Hello!"')  # シングルクォーテーション内でダブルクォーテーションを使用
print("She said, 'Hi!'")    # ダブルクォーテーション内でシングルクォーテーションを使用

# キャリッジリターンの使用例
print("Hello\rWorld")  # Helloを表示した後、カーソルを行の先頭に移動してからWorldを表示

# バックスペースの使用例
print("Hello\bWorld")  # Helloの後にバックスペースを使って1文字消してからWorldを表示
