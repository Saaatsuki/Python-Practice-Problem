# 참조 변수란(Reference variable)?
# 변수다! -> 메모리 공간에 값을 저장하는 것
# 일반변수(Primitive variable)과 차이점은?
# 일반변수와 달리, 참변수는 메모리 주소 값을 저장한다.

# 参照変数とは何ですか？
# 変数です！ -> 値をメモリ空間に保存するものです。
# プリミティブ変数との違いは？
# プリミティブ変数とは異なり、参照変数はメモリアドレスの値を保存します。
bar = [90, 10, 20]

def test(argValue):
    # リストの各要素に1を足す処理を行う関数
    # 引数としてリストを受け取り、そのリストの各要素を1ずつ増やす
    # 引数:
    #   argValue: 変更対象のリスト
    # 戻り値: なし（None）
    
    # リストの長さ分だけ繰り返す
    for index in range(0, len(argValue)):
        # リストの要素を1ずつ増やす
        argValue[index] += 1

# 関数の呼び出し
test(bar)

# リストの内容を表示
print(bar)  # [91, 11, 21]