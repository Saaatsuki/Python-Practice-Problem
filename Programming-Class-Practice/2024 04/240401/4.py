def addThreeNumbers(number1, number2, number3):
    totalSum = number1 + number2 + number3
    return totalSum

# 第1の呼び出し: パラメータは 1, 2, 3
sumOfFirstSet = addThreeNumbers(1, 2, 3)

# 第2の呼び出し: パラメータは 4, 5, 6
sumOfSecondSet = addThreeNumbers(4, 5, 6)

# 第3の呼び出し: パラメータは 7, 8, 9
sumOfThirdSet = addThreeNumbers(7, 8, 9)