n, m = map(int, input().split())

for i in range(1, n+1):
    for j in range(1, m+1):
        print(i, j)
        
        

# 最初に、プログラムは入力を受け取ります。
# ここでは、サイコロの面の数が1つ目のサイコロにn個、
# 2つ目のサイコロにm個あるとします。
# 例えば、「2 3」というように、スペースで区切られた2つの数字を
# 入力します。

# 次に、この入力された数値を使って、
# プログラムは2つのサイコロを振ったときの
# 全ての組み合わせを出力します。

# 最初のforループでは、1からnまでの数字を順番に取ります。
# ここでは、iという変数がそれを担当します。
# 例えば、nが3の場合、iは1, 2, 3と順に変化します。

# 次に、内側のforループがあります。このループでは、
# 1からmまでの数字を順番に取ります。
# ここでは、jという変数がそれを担当します。
# 例えば、mが6の場合、jは1, 2, ..., 6と順に変化します。

# そして、2つのforループで取得したiとjの組み合わせを出力します。
# 例えば、iが1でjが2の場合、出力は「1 2」となります。
# これは、1つ目のサイコロが1の目を示し、
# 2つ目のサイコロが2の目を示す状況を表します。

# 最終的に、すべての組み合わせが出力されると、
# プログラムは終了します。

# これにより、2つのサイコロを振った際の全ての組み合わせが分かります。