# 16進数を入力する
hex_num = input()

# 16進数を10進数に変換する
dec_num = int(hex_num, 16)

# 1から15までの数でループを実行する
for i in range(1, 16):
    # 乗算の結果を計算する
    result = dec_num * i
    # 16進数に変換して出力する
    print(hex_num + '*' + hex(i).upper()[2:] + '=' + hex(result).upper()[2:])
    
    
    
    
    
# 16進数の入力を受け取り、10進数に変換する
a = int(input(), 16)

# 1から15までの数でループを実行する
for i in range(1, 16):
    # 掛け算の結果を計算し、16進数で出力する
    # %X は、16進数の整数を大文字で出力するフォーマット指定子です。
    # "%X*%X=%X" は、フォーマット文字列であり、3つの変数を含みます。
    # フォーマット文字列内の %X は、それぞれ a、i、a*i に置き換えられます。
    # 例えば、a=10, i=3, a*i=30 の場合、"A*3=1E" という文字列が生成されます。
    print("%X*%X=%X" % (a, i, a*i))
