con = int(input())
sum_num = 0
for i in range(con+1):
    for j in range(i,con+1):
        sum_num += i+j
print(sum_num)

'''この問題では、Nのサイズのドミノセットのドミノの合計点数を計算します。各ドミノは2つの側面を持ち、それぞれの側面には0からNまでの点数がつきます。例えば、N=2のとき、ドミノセットは次のようになります:

(0,0), (0,1), (0,2)
(1,1), (1,2)
(2,2)
上記の例では、各ドミノに含まれる点数の合計を求めます。

入力
ドミノセットのサイズ N が与えられる。（1 ≤ N ≤ 1000）
出力
サイズが N のドミノセットに含まれる全ての点数の合計を出力します。
解法のポイント
ドミノセットの全てのドミノについて、点数の合計を計算する必要があります。各ドミノ (i, j) は、0 ≤ i ≤ j ≤ N の範囲で点数が割り当てられます。

計算方法
それぞれのドミノ (i, j) について、i + j を合計します。
0 ≤ i ≤ j ≤ N の全ての組み合わせについて合計します。'''